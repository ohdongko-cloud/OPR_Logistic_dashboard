/**
 * 지점명 정규화 — (−)재고 점포행 조인키(spec §4-1 키 이원화).
 *
 * 대시보드 점포행은 지점명(C)으로 수불오차를 조회한다(집계행은 코드 A). 점포명 철자·
 * 접미사(예 `_BGO`/`_BGT`) 변이가 미스매칭원 → 정규화로 흡수. 단 현재 실파일은 직영 14점
 * 카드가 코드·이름 모두 정합이므로 NFKC+trim+공백제거만으로 충분(접미사 점포는 카드 없음).
 *
 * 정규화 규칙(보수적 — 과도 제거로 다른 점포 충돌 방지):
 *   1) NFKC 정규화 + trim
 *   2) 모든 공백 제거(전각·반각)
 *  (접미사 `_BGx` 는 제거하지 않음 — 같은 베이스명 + 다른 접미사가 서로 다른 점포라
 *   제거 시 충돌. 카드 노출 점포는 접미사가 없어 영향 없음.)
 */
export function normalizeStoreName(name: string): string {
  return name.normalize("NFKC").trim().replace(/\s+/g, "");
}
